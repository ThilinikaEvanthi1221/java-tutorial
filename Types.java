public class Types {
    public static void main(String[] args) {
        // byte - 1 [-128 to 127]
        // short - 2
        // int -4 (1,2,3)
        // long - 8
        // float - 4 (3.14)
        // double - 8
        // char - 2 (a,b,c,d)
        // boolean - 1 (true/false)

        byte age = 36;
        int phone = 1234567890;
        long phone2 = 12345678900L;
        float pi = 3.14F;
        char letter = '@';
        boolean isAdult = false;

        // non-primitive
        String name = "Aman";
        System.out.println(name.length());
    }
}
